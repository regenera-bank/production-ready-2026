function walk(dir, acc = []) { for (const e of readdirSync(dir)) { if (EXCLUDE.has(e)) continue; const p = join(dir, e); const st = statSync(p); if (st.isDirectory()) walk(p, acc); else acc.push(p); } return acc; }

function tool_listar_arquivos({ subdir } = {}) {
  const base = subdir ? join(CFG.repo, subdir) : CFG.repo;
  if (!existsSync(base)) return `(Erro: o diretório ${subdir} não existe no repositório.)`;
  const files = walk(base).map((p) => relative(CFG.repo, p)).sort();
  return files.length ? files.join('\n') : '(diretório vazio)';
}
function tool_ler_arquivo({ caminho }) {
  const p = join(CFG.repo, caminho);
  if (!existsSync(p)) return `(Erro: o arquivo ${caminho} não foi encontrado.)`;
  let txt = readFileSync(p, 'utf8');
  if (txt.length > 24000) txt = txt.slice(0, 24000) + `\n... [truncado em 24k chars de ${txt.length} para economizar memória]`;
  return txt;
}
function tool_buscar_no_repo({ termo }) {
  if (!termo) return '(Erro: termo de busca vazio)';
  const re = new RegExp(termo.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'i');
  const hits = [];
  for (const p of walk(CFG.repo)) {
    if (!/\.(ts|tsx|js|cjs|mjs|sql|sh|ya?ml|json|md|html|css)$/i.test(p)) continue;
    try { readFileSync(p, 'utf8').split('\n').forEach((l, i) => { if (re.test(l)) hits.push(`${relative(CFG.repo, p)}:${i + 1}: ${l.trim().slice(0, 120)}`); }); } catch {}
    if (hits.length > 60) break;
  }
  return hits.length ? hits.slice(0, 60).join('\n') : `(Nenhum resultado encontrado para o termo: "${termo}")`;
}
function tool_auditar_seguranca() {
  const SEC = [[/AIza[0-9A-Za-z_\-]{20,}/, 'chave Google API exposta', 'Critical'], [/-----BEGIN [A-Z ]*PRIVATE KEY-----/, 'chave privada embutida', 'Critical'], [/jwt[-_ ]?secret/i, 'JWT secret', 'Critical'], [/(secret|token|password)\s*[:=]\s*['"][^'"]{8,}['"]/i, 'credencial literal', 'High']];
  const out = [];
  for (const p of walk(CFG.repo)) {
    if (!/\.(ts|tsx|js|cjs|mjs|sql|sh|ya?ml|json|html|env)$/i.test(p)) continue;
    try { readFileSync(p, 'utf8').split('\n').forEach((l, i) => { for (const [re, m, sev] of SEC) if (re.test(l)) out.push(`[${sev}] ${relative(CFG.repo, p)}:${i + 1} — ${m}`); if (/\b(TODO|FIXME|HACK)\b/.test(l) && !/@\w+|\(\w+\)|\d{4}-\d{2}-\d{2}/.test(l)) out.push(`[Medium] ${relative(CFG.repo, p)}:${i + 1} — TODO/FIXME órfão`); }); } catch {}
  }
  return out.length ? out.join('\n') : 'Auditoria limpa: nenhum segredo exposto ou TODO órfão encontrado.';
}
function tool_limpar_sinal_ia({ caminho, aplicar } = {}) {
  let targets = walk(CFG.repo).filter((p) => GATE_EXT.has(extname(p).toLowerCase()));
  if (caminho) targets = targets.filter((p) => relative(CFG.repo, p) === caminho || relative(CFG.repo, p).includes(caminho));
  if (targets.length === 0) return `(Erro: arquivo ${caminho} não encontrado ou sem extensão suportada.)`;
  let rew = 0, clean = 0, rej = 0; const lines = [];
  for (const p of targets) {
    const ext = extname(p).toLowerCase(); const orig = readFileSync(p, 'utf8'); const out = heuristicRewrite(orig, ext); const v = gateVerify(orig, out, ext);
    const rel = relative(CFG.repo, p);
    if (!v.ok) { rej += 1; lines.push(`✗ ${rel} (rejeitado: ${v.reason})`); continue; }
    if (out === orig) { clean += 1; continue; }
    rew += 1;
    if (aplicar) writeFileSync(p, out);
    else { const dest = join(CFG.repo, CFG.outDir, 'proposto', rel); mkdirSync(dirname(dest), { recursive: true }); writeFileSync(dest, out); }
    lines.push(`✎ ${rel}`);
  }
  const head = aplicar ? 'APLICADO NO REPOSITÓRIO' : `Modo DRY-RUN (propostas salvas em ${CFG.outDir}/proposto/)`;
  return `${head}\nResumo: reescritos: ${rew} | já limpos: ${clean} | rejeitados: ${rej}\n` + lines.slice(0, 40).join('\n');
}

