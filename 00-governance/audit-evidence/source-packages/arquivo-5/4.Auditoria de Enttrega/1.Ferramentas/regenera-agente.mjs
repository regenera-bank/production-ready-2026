async function chatStream(messages, { onToken } = {}) {
  const res = await fetch(`${CFG.url}/chat/completions`, {
    method: 'POST', headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ model: CFG.model, messages, tools: TOOLS, tool_choice: 'auto', temperature: CFG.temperature, stream: true }),
  });
  if (!res.ok) throw new Error(`HTTP ${res.status}: ${(await res.text()).slice(0, 200)}`);
  const reader = res.body.getReader(); const dec = new TextDecoder();
  let buf = '', content = ''; const toolAcc = [];
  while (true) {
    const { value, done } = await reader.read(); if (done) break;
    buf += dec.decode(value, { stream: true });
    let nl; while ((nl = buf.indexOf('\n')) >= 0) {
      const line = buf.slice(0, nl).trim(); buf = buf.slice(nl + 1);
      if (!line.startsWith('data:')) continue;
      const payload = line.slice(5).trim(); if (payload === '[DONE]') continue;
      let j; try { j = JSON.parse(payload); } catch { continue; }
      const delta = j.choices?.[0]?.delta ?? {};
      if (delta.content) { content += delta.content; if (onToken) onToken(delta.content); }
      if (delta.tool_calls) for (const tc of delta.tool_calls) { const idx = tc.index ?? 0; toolAcc[idx] ??= { id: tc.id, name: '', args: '' }; if (tc.id) toolAcc[idx].id = tc.id; if (tc.function?.name) toolAcc[idx].name += tc.function.name; if (tc.function?.arguments) toolAcc[idx].args += tc.function.arguments; }
    }
  }
  return { content, tool_calls: toolAcc.filter(Boolean) };
}

// O Otimizador de Memória: Evita que o LM Studio quebre com o limite de tokens
function truncateHistory(messages) {
  if (messages.length <= CFG.maxHistory) return messages;
  const sys = messages[0];
  const tail = messages.slice(-(CFG.maxHistory - 1));
  return [sys, ...tail];
}

async function agentTurn(messages) {
  for (let hop = 0; hop < CFG.maxToolHops; hop += 1) {
    let printedAny = false;
    const safeMessages = truncateHistory(messages);
    const { content, tool_calls } = await chatStream(safeMessages, { onToken: (t) => { if (!printedAny) { process.stdout.write(COL.cyan('\n[Arquiteto]> ')); printedAny = true; } process.stdout.write(t); } });
    
    if (tool_calls.length) {
      messages.push({ role: 'assistant', content: content || null, tool_calls: tool_calls.map((t, i) => ({ id: t.id || `call_${i}`, type: 'function', function: { name: t.name, arguments: t.args || '{}' } })) });
      for (const tc of tool_calls) {
        let argsObj = {}; try { argsObj = JSON.parse(tc.args || '{}'); } catch {}
        process.stdout.write(COL.dim(`\n  ⚙️ Sistema local executando: ${tc.name}(${JSON.stringify(argsObj)})\n`));
        let result; try { result = (DISPATCH[tc.name] ?? (() => `(Erro interno: ferramenta ${tc.name} não existe)`))(argsObj); } catch (e) { result = `(Falha crítica na ferramenta: ${e.message})`; }
        messages.push({ role: 'tool', tool_call_id: tc.id || 'call_0', content: String(result).slice(0, 10000) });
      }
      continue;
    }
    if (printedAny) process.stdout.write('\n\n');
    else process.stdout.write(COL.cyan('\n[Arquiteto]> ') + (content || COL.dim('...')) + '\n\n');
    messages.push({ role: 'assistant', content });
    return;
  }
  process.stdout.write(COL.red('\n[Alerta do Sistema: Agente forçado a parar por limite de ações (looping evitado)]\n\n'));
}

