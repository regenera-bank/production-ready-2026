async function probeConnection() {
  try { const r = await fetch(`${CFG.url}/models`); const d = await r.json(); const ids = (d?.data ?? []).map((m) => m.id); return { ok: true, ids }; }
  catch (e) { return { ok: false, err: e.message }; }
}

async function main() {
  console.clear();
  console.log(COL.bold('\n  REGENERA V2 · ARQUITETO SÊNIOR LOCAL'));
  console.log(COL.dim(`  Dossiê (Repo): ${CFG.repo}`));
  console.log(COL.dim(`  Motor Lógico:  ${CFG.model}`));
  
  const probe = await probeConnection();
  if (!probe.ok) { console.log(COL.red(`\n  [Erro de Rede] LM Studio não responde em ${CFG.url}`)); process.exit(1); }
  
  console.log(COL.green(`  Sistemas on-line. Modelos ativados: ${probe.ids.join(', ') || '(Nenhum)'}`));
  console.log(COL.dim('  Terminal de comandos rápidos: /limpar | /aplicar | /auditar | /limpartela | /sair\n'));

  const messages = [{ role: 'system', content: SYSTEM }];
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  const ask = () => rl.question(COL.bold('Comando> '), handle);

  async function handle(line) {
    const t = line.trim();
    if (!t) return ask();
    if (t === '/sair' || t === '/exit') { console.log(COL.dim('Desconectando.')); rl.close(); return; }
    if (t === '/limpartela') { console.clear(); return ask(); }
    if (t === '/modelo') { console.log(COL.dim(`modelo=${CFG.model}\nendpoint=${CFG.url}\nrepo=${CFG.repo}`)); return ask(); }
    if (t === '/auditar') { console.log(COL.dim('\n⚙️ Executando varredura...\n\n' + tool_auditar_seguranca() + '\n')); return ask(); }
    if (t === '/limpar' || t === '/aplicar') {
      const aplicar = t === '/aplicar';
      console.log(COL.yellow(`\n⚙️ Executando motor de limpeza (${aplicar ? 'GRAVANDO' : 'SIMULAÇÃO'})...`));
      console.log(COL.dim('\n' + tool_limpar_sinal_ia({ aplicar }) + '\n'));
      return ask();
    }
    
    messages.push({ role: 'user', content: t });
    try { await agentTurn(messages); } catch (e) { console.log(COL.red(`\n[Falha Crítica]: ${e.message}\n`)); }
    ask();
  }
  ask();
}

main().catch((e) => { console.error('Erro Fatal:', e.message); process.exit(1); });
