function stripCLike(src) { let o = '', i = 0; const n = src.length; let s = null; while (i < n) { const c = src[i], d = src[i + 1]; if (s) { o += c; if (c === '\\') { o += d ?? ''; i += 2; continue; } if (c === s) s = null; i += 1; continue; } if (c === "'" || c === '"' || c === '`') { s = c; o += c; i += 1; continue; } if (c === '/' && d === '/') { while (i < n && src[i] !== '\n') i += 1; continue; } if (c === '/' && d === '*') { i += 2; while (i < n && !(src[i] === '*' && src[i + 1] === '/')) i += 1; i += 2; continue; } o += c; i += 1; } return o; }
function stripSql(src) { let o = '', i = 0; const n = src.length; while (i < n) { const c = src[i], d = src[i + 1]; if (c === "'") { o += c; i += 1; while (i < n) { o += src[i]; if (src[i] === "'" && src[i + 1] === "'") { o += src[i + 1]; i += 2; continue; } if (src[i] === "'") { i += 1; break; } i += 1; } continue; } if (c === '$') { const m = /^\$[A-Za-z0-9_]*\$/.exec(src.slice(i)); if (m) { const t = m[0]; o += t; i += t.length; const e = src.indexOf(t, i); if (e === -1) { o += src.slice(i); i = n; } else { o += src.slice(i, e + t.length); i = e + t.length; } continue; } } if (c === '-' && d === '-') { while (i < n && src[i] !== '\n') i += 1; continue; } if (c === '/' && d === '*') { i += 2; while (i < n && !(src[i] === '*' && src[i + 1] === '/')) i += 1; i += 2; continue; } o += c; i += 1; } return o; }
function stripHashLike(src) { let o = '', i = 0; const n = src.length; let s = null; while (i < n) { const c = src[i]; if (s) { o += c; if (c === s) s = null; i += 1; continue; } if (c === "'" || c === '"') { s = c; o += c; i += 1; continue; } if (c === '#') { while (i < n && src[i] !== '\n') i += 1; continue; } o += c; i += 1; } return o; }
function codeOnly(src, ext) { let s; if (C_LIKE.has(ext)) s = stripCLike(src); else if (ext === '.sql') s = stripSql(src); else if (['.sh', '.yml', '.yaml'].includes(ext)) s = stripHashLike(src); else s = src; return s.replace(/\s+/g, ' ').trim(); }
const AI_VESTIGES = [/REGENERA ENTERPRISE SYSTEM/i, /\bAs an AI\b/i, /\bGenerated by (?:AI|GPT|Claude|Gemini|Copilot)\b/i, /\bv\d+\.\d+\b.*ENTERPRISE/i];
function gateVerify(orig, rew, ext) { if (rew.trim().length === 0) return { ok: false, reason: 'vazio' }; if (codeOnly(orig, ext) !== codeOnly(rew, ext)) return { ok: false, reason: 'código mudou (não só comentário)' }; for (const re of AI_VESTIGES) if (re.test(rew)) return { ok: false, reason: `vestígio de IA: ${re}` }; if (C_LIKE.has(ext)) { const c = stripCLike(rew); if ((c.match(/\{/g) || []).length !== (c.match(/\}/g) || []).length) return { ok: false, reason: 'chaves desbalanceadas' }; } return { ok: true, reason: 'só comentário mudou' }; }

const DIVIDER = /^\s*\/\/\s*[-=*_]{4,}\s*$/;
const THEATER = /(QUANTUM|CYBERPUNK|\bAGI\b|GOD-?TIER|NEURAL LOGIC ENGINE|ENTERPRISE SYSTEM|SINGULARITY|AI CONTEXT INJECTION|MODEL SELECTION STRATEGY|THEME CONFIG MAPS|RENDERERS)/i;
const LABEL_NOISE = new Set(['components', 'icons', 'extended modules', 'custom hooks', 'ui state', 'settings state', 'notification state', 'navigation state', 'form data', 'form validations', 'email form state', 'email submit handler', 'biometrics scan state', 'raphaela agi state', 'audio refs for gemini tts', 'tts', 'svg dimensions', 'create path', 'type filter', 'category filter', 'global search query', 'sequence timing', 'mouse parallax logic', 'execute', 'parse output', 'extract web intelligence', 'render', 'helpers', 'state', 'styles', 'types']);
function heuristicRewrite(src, ext) {
  if (!C_LIKE.has(ext)) return src.split('\n').filter((l) => !/^\s*[#-]{2}\s*[-=*_]{6,}\s*$/.test(l)).join('\n').replace(/\n{3,}/g, '\n\n');
  let o = '', i = 0; const n = src.length; let s = null;
  const decide = (t) => { const x = t.replace(/\s+$/, ''); const b = x.replace(/^\s*\/\/\s?/, '').trim().toLowerCase(); if (DIVIDER.test(x)) return { drop: 1 }; if (LABEL_NOISE.has(b)) return { drop: 1 }; if (THEATER.test(x)) return { drop: 1 }; return {}; };
  while (i < n) {
    const c = src[i], d = src[i + 1];
    if (s) { o += c; if (c === '\\') { o += d ?? ''; i += 2; continue; } if (c === s) s = null; i += 1; continue; }
    if (c === "'" || c === '"' || c === '`') { s = c; o += c; i += 1; continue; }
    if (c === '/' && d === '/') { let j = i + 2; while (j < n && src[j] !== '\n') j += 1; const cm = src.slice(i, j); let ls = i - 1; while (ls >= 0 && src[ls] !== '\n' && (src[ls] === ' ' || src[ls] === '\t')) ls -= 1; const full = ls < 0 || src[ls] === '\n'; if (decide(cm).drop) { o = o.replace(/[ \t]*$/, ''); i = full && j < n && src[j] === '\n' ? j + 1 : j; continue; } o += cm; i = j; continue; }
    if (c === '/' && d === '*') { let j = i + 2; while (j < n && !(src[j] === '*' && src[j + 1] === '/')) j += 1; j = Math.min(j + 2, n); const bl = src.slice(i, j); let ls = i - 1; while (ls >= 0 && src[ls] !== '\n' && (src[ls] === ' ' || src[ls] === '\t')) ls -= 1; const full = ls < 0 || src[ls] === '\n'; if (/SPDX-License-Identifier|@license/.test(bl)) { o = o.replace(/[ \t]*$/, ''); i = full && j < n && src[j] === '\n' ? j + 1 : j; continue; } o += bl; i = j; continue; }
    o += c; i += 1;
  }
  return o.replace(/\n{3,}/g, '\n\n');
}

